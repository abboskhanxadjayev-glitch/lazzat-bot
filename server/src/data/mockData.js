function buildProductId(categoryIndex, itemIndex) {
  const categoryDigit = String(categoryIndex);
  const tail = String(itemIndex).padStart(12, "0");
  return `${categoryDigit.repeat(8)}-${categoryDigit.repeat(4)}-${categoryDigit.repeat(4)}-${categoryDigit.repeat(4)}-${tail}`;
}

function buildProductDescription(category, item) {
  return `${item.name} - ${category.productDescription}`;
}

export const categories = catalogDefinition.map((category, categoryIndex) => ({
  id: category.id,
  slug: category.slug,
  name: category.name,
  description: category.description,
  sortOrder: categoryIndex + 1
}));

export const products = catalogDefinition.flatMap((category, categoryIndex) =>
  category.items.map((item, itemIndex) => ({
    id: buildProductId(categoryIndex + 1, itemIndex + 1),
    categoryId: category.id,
    name: item.name,
    description: buildProductDescription(category, item),
    price: item.price,
    isPopular: Boolean(item.isPopular),
    sortOrder: itemIndex + 1
  }))
);
